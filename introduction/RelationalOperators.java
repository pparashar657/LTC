package introduction;

public class RelationalOperators {

	public static void main(String[] args) {
		System.out.println(1 < 2);
		System.out.println(10 < 2);
		System.out.println(1 <= 2);
		System.out.println(2 <= 2);
		
		System.out.println(10 > 2);
		System.out.println(1 >= 2);
		System.out.println(2 >= 2);
		
		System.out.println(20 == 20.0);
		System.out.println(20 != 20.0);
		
		
	}

}
