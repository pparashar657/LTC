package introduction;

public class IfElse {

	public static void main(String[] args) {
		if (true) {
			System.out.println("I am true");
		} else {
			System.out.println("I am false");
		}
		
		int a = 17;
		
		if (a < 10) {
			System.out.println("a is less than 10");
		} else {
			System.out.println("a is not less than 10");
		}
	}

}
